package string;

// toUppercase() = it is used to convert lower case value to upper case
public class toUppercase {
	public static void main(String[] args) {
		String s = "ashwini";
		
		System.out.println(s.toUpperCase());
		
		//toLovercase() - use to convert upper into Lovercase
		String m = "ASHISH";
		System.out.println(m.toLowerCase());
	}

}
