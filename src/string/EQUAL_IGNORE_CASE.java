package string;
// IT WILL USE TO COMPARE STRING IGNORING CASES

public class EQUAL_IGNORE_CASE {

	public static void main(String[] args) {
		String s = "Velocity";
		String s1 = "velocity";
		
		System.out.println(s.equalsIgnoreCase(s1));
		
		
		System.out.println(s.equalsIgnoreCase(s));
	
	}
}
