package logical_program;

public class Print_1_to_100 {
	public static void main(String[] args) {
		// print no. from 1 to 100
		for(int i=1; i<= 100; i++) {
			System.out.print(i);
		}
		
		// print no. from 100 to 1
					for(int i=100;  i>=1; i--) {
						 System.out.print(i);
					}
				
		
	}

}
